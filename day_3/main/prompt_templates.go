// 快速生成场景Prompt（替换占位符）
func GeneratePrompt(scene string, params map[string]string) string {
	prompt, ok := EnterprisePromptTemplates[scene]
	if !ok {
		return "场景不存在"
	}

	// 替换占位符（比如 {{contract_content}}）
	for key, value := range params {
		prompt = strings.ReplaceAll(prompt, "{{"+key+"}}", value)
	}

	return prompt
}
